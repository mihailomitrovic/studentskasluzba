    private void btnOdjaviIspitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOdjaviIspitActionPerformed
        int red = tblPrijave.getSelectedRow();
        
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita prijavu! Izaberite red!");
            return;
        } else {            
            try {
                ModelTabelePrijava mtp = (ModelTabelePrijava) tblPrijave.getModel();
                PrijavaIspita prijava = mtp.getPrijava(red);
                
                PrijavaIspita p = new PrijavaIspita(prijava.getPrijavaID(), new Date(), null, null, null, null);
                PrijavaIspita parametar = KlijentKontroler.getInstance().ucitajPrijavuIspita(p);
                KlijentKontroler.getInstance().obrisiPrijavuIspita(parametar);
                
                Object[] opcije = { "Da", "Ne" };
                int izbor = JOptionPane.showOptionDialog(null, "Da li želite da obrišete prijavu?", "Brisanje prijave",JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, opcije, opcije[0]);
                
                if (izbor == 0) {
                    try {
                        JOptionPane.showMessageDialog(this, "Sistem je obrisao prijavu!");
                        osveziTabelu();
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this, "Sistem ne može da obriše prijavu!");
                    }
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da učita prijavu!");
            }
        }
    }//GEN-LAST:event_btnOdjaviIspitActionPerformed

    private void btnIzdajRacunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzdajRacunActionPerformed
        int red = tblPrijave.getSelectedRow();
        
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita prijavu! Izaberi red!");
            return;
        } else {
            try {                
                ModelTabelePrijava mtp = (ModelTabelePrijava) tblPrijave.getModel();
                PrijavaIspita prijava = mtp.getPrijava(red);
                
                PrijavaIspita p = new PrijavaIspita(prijava.getPrijavaID(), new Date(), null, null, null, null);
                PrijavaIspita parametar = KlijentKontroler.getInstance().ucitajPrijavuIspita(p);
                JOptionPane.showMessageDialog(this, "Sistem je učitao prijavu!");
                
                try {
                    RacunPrijaveIspita racun = KlijentKontroler.getInstance().vratiRacunPrijaveIspita(prijava);
                    
                    if (racun != null) {
                        JOptionPane.showMessageDialog(this, "Sistem je već kreirao račun za ovu prijavu!");
                        return;
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Sistem ne može da učita prijavu!");
                }
                
                Object[] opcije = { "Da", "Ne" };
                int izbor = JOptionPane.showOptionDialog(null, "Da li želite da izdate račun?", "Izdavanje računa",JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, opcije, opcije[0]);
                
                if (izbor == 0) {
                    try {
                        JOptionPane.showMessageDialog(this, "Sistem nije ranije kreirao račun za ovu prijavu!");
                        FormaRacun forma = new FormaRacun(this, true, parametar);
                        forma.setVisible(true);
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this, "Sistem ne može da učita prijavu!");
                    }
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da učita prijavu!");
            }
        }
    }//GEN-LAST:event_btnIzdajRacunActionPerformed

