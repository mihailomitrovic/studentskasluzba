    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        if (txtPretraga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite parametar za pretragu!");
            return;
        }
        
        try {
            Ispit i = new Ispit(-1, txtPretraga.getText(), "", 0, 0);
            Student s = new Student(-1, txtPretraga.getText(), new Date(), "", null);
            PrijavaIspita p = new PrijavaIspita(-1, new Date(), null, i, s, sesija.Sesija.getInstance().getUlogovani());
            ArrayList<PrijavaIspita> listaPronadjenih = KlijentKontroler.getInstance().nadjiPrijaveIspita(p);
            tblPrijave.setModel(new ModelTabelePrijava(listaPronadjenih));
            
            if (listaPronadjenih.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Sistem ne može da nađe prijave po zadatoj vrednosti");
                    txtPretraga.setText(null);
                    ArrayList<PrijavaIspita> praznaLista = new ArrayList<>();
                    tblPrijave.setModel(new ModelTabelePrijava(praznaLista));
            } else {
                    JOptionPane.showMessageDialog(this, "Sistem je našao prijave po zadatoj vrednosti");
                    tblPrijave.setModel(new ModelTabelePrijava(listaPronadjenih));
            }     
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da nađe prijave po zadatoj vrednosti");
        }
        
        txtPretraga.setText(null);
    }//GEN-LAST:event_btnPretraziActionPerformed

