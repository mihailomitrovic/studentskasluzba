    private void srediFormu() throws Exception {
        txtImePrezime.setText(student.getImePrezime());
        txtKontakt.setText(student.getKontakt());
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        txtDatumRodjenja.setText(sdf.format(student.getDatumRodjenja()));
        
        cmbMesto.removeAllItems();
        for (Mesto mesto : KlijentKontroler.getInstance().ucitajListuMesta()) {
            cmbMesto.addItem(mesto);
        }
        cmbMesto.getModel().setSelectedItem(student.getMesto());  
    }
