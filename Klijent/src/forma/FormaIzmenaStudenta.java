    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        String imePrezime = txtImePrezime.getText();
        String datum = txtDatumRodjenja.getText();
        String kontakt = txtKontakt.getText();
        Mesto mesto = (Mesto) cmbMesto.getSelectedItem();

        if (imePrezime.isEmpty() || datum.isEmpty() || kontakt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja su obavezna!");
            return;
        }
        
        if (!imePrezime.contains(" ")) {
            JOptionPane.showMessageDialog(this, "Moraju se uneti i ime i prezime!");
            return;        
        }
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        Date datumRodjenja;
        try {
            datumRodjenja = sdf.parse(datum);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Format datuma nije ispravan!");
            return;
        }
        
        if (datumRodjenja.after(new Date())) {
            JOptionPane.showMessageDialog(this, "Datum rođenja ne može biti posle današnjeg datuma");
            return;
        }
        
        if (txtKontakt.getText().length() > 9) {
            JOptionPane.showMessageDialog(this, "Kontakt telefon ne može imati više od 9 cifara!");
            return;        
        }
        
        for (int i = 0; i < txtKontakt.getText().length(); i++) {
            if (Character.isLetter(txtKontakt.getText().charAt(i))) {
                JOptionPane.showMessageDialog(this, "Kontakt telefon ne može sadržati cifre!");
                return;
            }
        }

        try {
            student.setImePrezime(imePrezime);
            student.setDatumRodjenja(datumRodjenja);
            student.setKontakt(kontakt);
            student.setMesto(mesto);
            
            KlijentKontroler.getInstance().izmeniStudenta(student);
            JOptionPane.showMessageDialog(this, "Sistem je izmenio studenta!");
            FormaPretragaStudenata fps = (FormaPretragaStudenata) getParent();
            fps.osveziTabelu();
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da izmeni studenta!");
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

