    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        int red = tblStudenti.getSelectedRow();
        
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita studenta! Izaberite red!");
        } else {
            ModelTabeleStudent mts = (ModelTabeleStudent) tblStudenti.getModel();
            Student student = mts.getStudent(red);
            try {
                Student s = new Student(student.getStudentID(), "", new Date(), "", null);
                Student parametar = KlijentKontroler.getInstance().ucitajStudenta(s);
                JOptionPane.showMessageDialog(this, "Sistem je učitao studenta!");
                new FormaIzmenaStudenta(this, true, parametar).setVisible(true);
            } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita studenta!");
            }
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnPrijavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrijavaActionPerformed
        int red = tblStudenti.getSelectedRow();
        
        if (red < 0) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita studenta! Izaberite red!");
        } else {
            ModelTabeleStudent mts = (ModelTabeleStudent) tblStudenti.getModel();
            Student student = mts.getStudent(red);
            try {
                Student s = new Student(student.getStudentID(), "", new Date(), "", null);
                Student parametar = KlijentKontroler.getInstance().ucitajStudenta(s);
                JOptionPane.showMessageDialog(this, "Sistem je učitao studenta!");
                new FormaPrijavaIspita(this, true, parametar).setVisible(true);
            } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita studenta!");
            }
        }
    }//GEN-LAST:event_btnPrijavaActionPerformed

