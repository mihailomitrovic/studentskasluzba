    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        if (txtPretraga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite parametar!");
            return;
        }
                
        try {
            Student s = new Student(-1, txtPretraga.getText(), new Date(), "", null);
            ArrayList<Student> listaPronadjenih = KlijentKontroler.getInstance().nadjiStudente(s);
            tblStudenti.setModel(new ModelTabeleStudent(listaPronadjenih));
            
            if (listaPronadjenih.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Sistem ne može da nađe studente po zadatoj vrednosti");
                    txtPretraga.setText(null);
                    ArrayList<Student> praznaLista = new ArrayList<>();
                    tblStudenti.setModel(new ModelTabeleStudent(praznaLista));
            } else {
                    JOptionPane.showMessageDialog(this, "Sistem je našao studente po zadatoj vrednosti");
                    tblStudenti.setModel(new ModelTabeleStudent(listaPronadjenih));
            }     
        } catch (Exception ex) {
             JOptionPane.showMessageDialog(this, "Sistem ne može da nađe studente po zadatoj vrednosti");
        }
        
        txtPretraga.setText(null);
    }//GEN-LAST:event_btnPretraziActionPerformed

