    private void btnPrikaziInformacijeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziInformacijeActionPerformed
        int red = tblIspiti.getSelectedRow();
        
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita ispit! Izaberite red!");
        } else {
            ModelTabeleInformacija mti = (ModelTabeleInformacija) tblIspiti.getModel();
            Ispit ispit = mti.getIspit(red);
            try {
                Ispit i = new Ispit(ispit.getIspitID(), "", "", 0, 0);
                Ispit parametar = KlijentKontroler.getInstance().ucitajIspit(i);
                JOptionPane.showMessageDialog(this, "Sistem je učitao ispit!");
                new FormaInformacije(this, true, parametar).setVisible(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da učita ispit!");
            }
        }
    }//GEN-LAST:event_btnPrikaziInformacijeActionPerformed

