    private void btnPretražiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretražiActionPerformed
        if (txtPretraga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite parametar za pretragu!");
            return;
        }
        
        try {
            Ispit i = new Ispit(-1, txtPretraga.getText(), "", 0, 0);
            ArrayList<Ispit> listaPronadjenih = KlijentKontroler.getInstance().nadjiIspite(i);
            tblIspiti.setModel(new ModelTabeleInformacija(listaPronadjenih));
            
            if (listaPronadjenih.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Sistem ne može da nađe ispite po zadatoj vrednosti!");
                    txtPretraga.setText(null);
                    ArrayList<Ispit> praznaLista = new ArrayList<>();
                    tblIspiti.setModel(new ModelTabeleInformacija(praznaLista));
            } else {
                    JOptionPane.showMessageDialog(this, "Sistem je našao ispite po zadatoj vrednosti!");
                    tblIspiti.setModel(new ModelTabeleInformacija(listaPronadjenih));
            }     
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da nađe ispite po zadatoj vrednosti!");
        }
        
        txtPretraga.setText(null);
    }//GEN-LAST:event_btnPretražiActionPerformed

